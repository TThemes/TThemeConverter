package types

import "time"

// ThemeParams is a struct containing theme parameters to read or write
type ThemeParams struct {
	Divider    string
	FileName   string
	Eol        string
	Eols       string
	Comment    string
	Argb       bool
	ImgEmbeded bool
	IsZip      bool
}

