// GetThemeParams returns ThemeParams for OS
func GetThemeParams(os string) ThemeParams {
	params := ThemeParams{}
	switch os {
	case "desktop":
		{
			params.Divider = ": "
			params.Eol = ";"
			params.Eols = ";\n"
			params.Comment = "//"
			params.FileName = "colors.tdesktop-theme"
			params.Argb = false
		}
	case "android":
		{
			params.Divider = "="
			params.Eol = "\n"
			params.Eols = "\n"
			params.Comment = "//"
			params.FileName = "theme.attheme"
			params.Argb = true
		}
	case "ios":
		{

		}
	}
	return params
}
